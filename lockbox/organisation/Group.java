package org.vigilance.lockbox.organisation;

import java.util.ArrayList;

/**
 * @author Declan R.A Wadsworth
 * @version 0.1
 * Java class that extends java.util.ArrayList and provides a name for the array itself, so users can define groups
 * @param <E> a class to instantiate the arraylist with
 */
public class Group<E> extends ArrayList<E> {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String name;
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Group(String name){
        this.name = name;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
